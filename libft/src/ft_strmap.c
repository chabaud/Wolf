#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*tmp;
	int		i;

	i = -1;
	if (!(tmp = (char*)ft_memalloc(ft_strlen(s) + 1)))
		return (tmp);
	if (!s)
		return (tmp);
	while (s[++i])
		tmp[i] = f(s[i]);
	return (tmp);
}
